// -----------------------------------------------------------------------------
// Requirements
// -----------------------------------------------------------------------------

var redis = require('redis');

var clients = {};

// -----------------------------------------------------------------------------
// Public
// -----------------------------------------------------------------------------

function createClient (port, host, options) {
	clients.default = redis.createClient(port, host, options);
}

function client () {
	return clients.default;
}

// -----------------------------------------------------------------------------
// Exports
// -----------------------------------------------------------------------------

exports.createClient = createClient;
exports.client = client;